     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        if ( get_id_or_slug( $id ) == 'id' ) {
            $group = Group::find( $id );
        } else {
            $group = Group::findBySlug( $id );
        }

        $posts = $group->post;
        return view('public.'.$this->template_name.'.group.list')->with(compact('group', 'posts'));
    }

